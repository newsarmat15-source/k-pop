-- Память айдола о фанате (2026-07-23).
-- Документированная причина №1 оттока в компаньон-приложениях — «приходится заново
-- рассказывать боту базовые вещи о себе» (обзор PMC12575814, 2025). История в
-- chat_messages это НЕ решает: в контекст влезают только последние 40 сообщений,
-- всё, что фанат рассказал о себе раньше, для айдола перестаёт существовать.
--
-- Формат намеренно примитивный: плоские пары ключ→значение, 25 штук максимум на айдола
-- (~300 токенов в системный промпт). Не профиль, не граф — короткая записная книжка друга:
-- как зовут, откуда, кто биас, что любит есть, чем болел на той неделе, что уже выучил.
-- Наполняется отдельным дешёвым вызовом-экстрактором раз в ~5 сообщений (см. lib/reply.js),
-- параллельно основному ответу, чтобы не добавлять задержки в диалог.
create table if not exists idol_memory (
  idol_id uuid not null references idols(id) on delete cascade,
  key text not null,                        -- 'name', 'city', 'bias', 'job', 'likes_food', ...
  value text not null,                      -- короткая фраза, не абзац
  updated_at timestamptz not null default now(),
  primary key (idol_id, key)                -- апсерт по (idol_id, key): факт обновляется, не дублируется
);

-- Выбор «последних N фактов» и обрезка самых старых при переполнении.
create index if not exists idol_memory_recent_idx on idol_memory(idol_id, updated_at desc);

alter table idol_memory enable row level security;  -- доступ только через service_role в api/*
