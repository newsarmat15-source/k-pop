-- База фактов о фанате (2026-07-24). Заменяет плоскую записную книжку idol_memory.
--
-- Требование Сармата дословно: «мы намереваемся, чтобы пользователи оставались с нами
-- НАВСЕГДА, и он должен помнить все факты, которые тот говорит о себе. База, где хранятся
-- не разговоры, а просто факты о человеке. "Сходил за хлебом, потом на тренировку" — это
-- не факт, записывать не надо. "Закончил школу на год раньше", "в детстве была собака с
-- таким-то именем" — это факт».
--
-- Что было не так со старой таблицей idol_memory:
--   1. Ключ был первичным ключом → ОДНО значение на ключ. Вторая собака затирала первую,
--      вторая любимая песня — первую. Накопления не происходило в принципе.
--   2. Потолок 25 фактов с УДАЛЕНИЕМ самых старых. Ровно то, чего нельзя делать, если цель —
--      «навсегда»: тихо забывалось самое давнее, то есть самое личное.
--   3. Вечное и сиюминутное лежали вперемешку (`pet` рядом с `mood_recent`), и настроение
--      недельной давности вытесняло имя детской собаки.
--
-- Здесь: строка = один факт. Вечное не удаляется никогда, сиюминутное живёт днями.
create table if not exists idol_facts (
  id bigserial primary key,
  idol_id uuid not null references idols(id) on delete cascade,
  -- 'stable' — то, что верно и через год (город, работа, детство, вкусы, важные люди).
  -- 'fresh'  — то, что верно сегодня (настроение, планы на неделю, незакрытый сюжет).
  kind text not null default 'stable' check (kind in ('stable', 'fresh')),
  slot text not null,                       -- name, city, childhood, pet, bias, job, note, ...
  value text not null,                      -- короткая фраза, не абзац
  first_seen timestamptz not null default now(),
  last_seen timestamptz not null default now(),
  hits int not null default 1,              -- сколько раз всплывало: чем чаще, тем важнее человеку
  unique (idol_id, slot, value)             -- один и тот же факт не дублируется, а обновляет last_seen
);

create index if not exists idol_facts_pick_idx on idol_facts(idol_id, kind, hits desc, last_seen desc);

alter table idol_facts enable row level security;  -- доступ только через service_role в api/*

