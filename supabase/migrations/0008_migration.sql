-- Переезд в мессенджер (2026-07-21). Айдол сама зовёт юзера привязать ОДИН канал,
-- проактив идёт строго туда (там работает пуш), веб остаётся полноценным и с той же памятью.
-- Приглашение — не жёсткий счётчик: код лишь ставит рамки (право позвать + тормоз),
-- сам МОМЕНТ выбирает айдол по ходу разговора.

-- Куда слать проактив: 'web' по умолчанию (пуша нет), после привязки — выбранный мессенджер.
-- Ровно один канал на юзера, чтобы не дублировать сообщения.
alter table training_stats add column if not exists primary_channel text not null default 'web';

-- Тормоз приглашений: сколько раз айдол уже звала перейти и когда в последний раз.
-- Кап (макс 2) + пауза (3 дня) держатся на этих полях, чтобы не наседать.
alter table training_stats add column if not exists messenger_invites int not null default 0;
alter table training_stats add column if not exists last_invite_at timestamptz;
