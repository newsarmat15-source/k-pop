const { data: items, error } = await db.from("ops_items").select("id,title");
if (error) { console.error(error.message); process.exit(1); }

let n = 0, miss = [];
for (const [prefix, progress, report] of R) {
  const it = items.find((i) => i.title.startsWith(prefix));
  if (!it) { miss.push(prefix); continue; }
  const { error: e } = await db.from("ops_items")
    .update({ progress, report, report_at: new Date().toISOString(), updated_at: new Date().toISOString() })
    .eq("id", it.id);
  if (e) { console.error(prefix, e.message); continue; }
  n++;
}
