// Проверка движка произношения на случаях, где написание расходится со звучанием.
// Эталоны — из правил корейской фонетики и из примеров, заложенных в api/song.js.
import { pronounce, transcribeLatin, transcribeCyrillic } from "../lib/ko-g2p.js";

const CASES = [
  // слово        звучит   латиница            кириллица
  ["궁금해",      "궁그매", "gung·geu·mae",     "гун·гы·мэ"],
  ["눈이",        "누니",   "nu·ni",            "ну·ни"],
  ["좋아",        "조아",   "jo·a",             "джо·а"],
  ["같이",        "가치",   "ga·chi",           "га·чи"],
  ["굳이",        "구지",   "gu·ji",            "гу·джи"],
  ["학교",        "학꾜",   "hak·kkyo",         "хак·кё"],
  ["잡고",        "잡꼬",   "jap·kko",          "джап·ко"],
  ["있다",        "읻따",   "it·tta",           "ит·та"],
  ["이렇게",      "이러케", "i·reo·ke",         "и·ро·ке"],
  ["말하니까",    "마라니까","ma·ra·ni·kka",    "ма·ра·ни·кка"],
  ["신라",        "실라",   "sil·la",           "щиль·ла"],
  ["국물",        "궁물",   "gung·mul",         "гун·муль"],
  ["입니다",      "임니다", "im·ni·da",         "им·ни·да"],
  ["시간",        "시간",   "si·gan",           "щи·ган"],
  ["마찬가지",    "마찬가지","ma·chan·ga·ji",   "ма·чан·га·джи"],
  ["네가",        "니가",   "ni·ga",            "ни·га"],   // певческая вольность
];

// Известный пробел, сознательно не закрыт: вставка ㄴ на стыке частей сложного
// слова (설국열차 → 설궁녈차). Отличить её от обычного переноса патчхима
// (눈이 → 누니) без словаря нельзя, а частицы в текстах встречаются на порядок
// чаще сложных слов — поэтому по умолчанию переносим, а не вставляем.
const KNOWN_GAP = ["설국열차"];

let bad = 0;
for (const [w, p, lat, cyr] of CASES) {
  const gp = pronounce(w), gl = transcribeLatin(w), gc = transcribeCyrillic(w);
  const ok = gp === p && gl === lat && gc === cyr;
  if (!ok) {
    bad++;
    console.log(`ПРОВАЛ ${w}`);
    if (gp !== p)   console.log(`   звучание: ждали ${p}   получили ${gp}`);
    if (gl !== lat) console.log(`   латиница: ждали ${lat}   получили ${gl}`);
    if (gc !== cyr) console.log(`   кириллица: ждали ${cyr}   получили ${gc}`);
  }
}
console.log(bad ? `\nпровалов: ${bad} из ${CASES.length}` : `все ${CASES.length} случаев сходятся`);
process.exit(bad ? 1 : 0);
